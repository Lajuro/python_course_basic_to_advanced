# 16 - Operadores Aritméticos

"""
    + - Adição
    - - Subtração
    * - Multiplicação
    / - Divisão
    // - Divisão que retorna inteiro
    ** - Exponenciação
    % - Resto da divisão
    ()
"""

print(10 + 2)  # Adição
print(10 - 2)  # Subtração
print(10 * 2)  # Multiplicação
print(10 / 2)  # Divisão

print(10 * "Roberto, ")  # Repete a exibição de uma string x

print(5 // 2)  # Exibe apenas o número inteiro da divisão.
print(5 / 2)  # Mostra as casas decimais
print(10 % 3)  # Mostra o resto da divisão

print(2**2)  # 2 ao quadrado
print(2**3)  # 2 ao cubo
